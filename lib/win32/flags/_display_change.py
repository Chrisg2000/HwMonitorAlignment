import enum


class DisplayChange(enum.IntEnum):
    RESTART = 1
    SUCCESSFUL = 0
    FAILED = -1
    BADMODE = -2
    NOTUPDATED = -3
    BADFLAGS = -4
    BADPARAM = -5
    BADDUALVIEW = -6


class ChangeDisplaySettings(enum.IntFlag):
    UPDATEREGISTRY = 0x00000001
    TEST = 0x00000002
    FULLSCREEN = 0x00000004
    GLOBAL = 0x00000008
    SET_PRIMARY = 0x00000010
    VIDEOPARAMETERS = 0x00000020
    ENABLE_UNSAFE_MODES = 0x00000100
    DISABLE_UNSAFE_MODES = 0x00000200
    RESET = 0x40000000
    RESET_EX = 0x20000000
    NORESET = 0x10000000


class ChangeDisplayReturn(enum.IntEnum):
    SUCCESSFUL = 0
    RESTART = 1
    FAILED = -1
    BADMODE = -2
    NOTUPDATED = -3
    BADFLAGS = -4
    BADPARAM = -5
    BADDUALVIEW = -6
